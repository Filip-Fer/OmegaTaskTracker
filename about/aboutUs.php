<h1>About Us</h1>

<p>ToDo Tracker je jednoduchá webová aplikace pro správu úkolů, která umožňuje uživatelům snadno organizovat své úkoly a sledovat jejich stav. S naší aplikací mohou uživatelé snadno vytvářet nové úkoly, nastavovat termíny splnění a přiřazovat úkoly kategoriím (tagům). Naše aplikace také umožňuje uživatelům sdílet své úkoly s kolegy a spolupracovat na jejich dokončení. </p>

<h2>Funkce projektu</h2>
<ul>
    <li>Vytváření nových úkolů</li>
    <li>Přiřazování úkolů kategoriím</li>
    <li>Nastavení termínů splnění úkolů</li>
    <li>Možnost sdílení úkolů s ostatními uživateli</li>
    <li>Možnost komentování úkolů</li>
    <li>Možnost nastavení priorit jednotlivých úkolů</li>
    <li>Přehledné a intuitivní uživatelské rozhraní</li>
</ul>

<h2>Technologie projektu</h2>
<ul>
    <li>Jazyk: PHP, JavaScript</li>
    <li>Databáze: MySQL</li>
    <li>Front-end: HTML, CSS, JavaScript</li>
</ul>

<p>ToDo Tracker vznikl jako projekt pro usnadnění organizace úkolů a práce s nimi. Věříme, že naše aplikace bude užitečná nejen pro jednotlivé uživatele, ale také pro týmy a firmy, které chtějí lépe organizovat svou práci a spolupracovat na projektech. Pokud máte nějaké dotazy nebo připomínky, neváhejte nás kontaktovat.</p>
